/**
 * Helper function to render an icon with optional classes and attributes
 * @param {string} name - Icon name from Icons object
 * @param {number} size - Icon size in pixels
 * @param {string} className - Additional CSS classes
 * @param {object} attrs - Additional HTML attributes
 * @returns {string} HTML string with icon
 */
function renderIcon(name, size = 24, className = '', attrs = {}) {
  if (!Icons[name]) {
// console.warn(`Icon "${name}" not found`);
    return '';
  }

  const iconSvg = Icons[name](size);
  const attrString = Object.entries(attrs)
    .map(([key, value]) => `${key}="${value}"`)
    .join(' ');

  return `<span class="icon ${className}" ${attrString}>${iconSvg}</span>`;
}

